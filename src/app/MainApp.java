    private void btn_CetakFilterSchedule1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_CetakFilterSchedule1ActionPerformed
        // TODO add your handling code here:
        String reportSource = null;
        String reportDest = null;

        try {
            Connection Koneksi = (Connection) DriverManager.getConnection("jdbc:mysql://localhost:3306/db_emerad", "root", "");
            com.mysql.jdbc.Connection c = (com.mysql.jdbc.Connection) Koneksi;
            reportSource = System.getProperty("user.dir") + "/src/report/report1.jrxml";
            reportDest = System.getProperty("user.dir") + "/src/report/report1.jasper";

            JasperReport jasperReport = JasperCompileManager.compileReport(reportSource);
            JasperPrint jasperPrint = JasperFillManager.fillReport(jasperReport, null, c);
            JasperExportManager.exportReportToHtmlFile(jasperPrint, reportDest);
            JasperViewer.viewReport(jasperPrint, false);

        } catch (SQLException | JRException e) {
            System.out.println(e);
        }
    }//GEN-LAST:event_btn_CetakFilterSchedule1ActionPerformed

