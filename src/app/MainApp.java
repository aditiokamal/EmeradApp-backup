    private void btn_FilterScheduleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_FilterScheduleActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Tanggal");
        model.addColumn("NIK");
        model.addColumn("Nama Karyawan");
        model.addColumn("Divisi");
        model.addColumn("Jabatan");
        model.addColumn("Tipe Absen");
                
        //menampilkan data ke database
        try {            
            String sql = "SELECT schedule_date, emp_id, emp_name, div_name, position_name, att_type FROM tb_tr_schedule where date(schedule_date)='"+txt_schdl_date1.getText()+"' AND att_type='"+cb_schdl_att_type1.getSelectedItem()+"'";
            
            java.sql.Connection conn = (Connection) Config.configDB();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet res = stm.executeQuery(sql);
            while (res.next()) {
                model.addRow(new Object[]{res.getString("schedule_date"), res.getString("emp_id"), res.getString("emp_name"), res.getString("div_name"), res.getString("position_name"), res.getString("att_type")});
            
            }
            view_filter_tb_tr_schedule.setModel(model);
        }
        catch (Exception e) {
        }
    }//GEN-LAST:event_btn_FilterScheduleActionPerformed
    
